"""
Module that includes number operations.
"""

def sum_number(x, y):
    """ Sum two numbers.

    Parameters
    ----------
    x : int, float
        First number to be summed.
    y : int, float
        Second number to be summed.

    Returns
    -------
    int, float
        The sum of the two numbers supplied.

    """
    return x + y


def _diff_number(x, y):
    """ Sum two numbers.

    Parameters
    ----------
    x : int, float
        First number to be summed.
    y : int, float
        Second number to be summed.

    Returns
    -------
    int, float
        The sum of the two numbers supplied.

    """
    return x - y
